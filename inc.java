// increment decrement operator
package NEW;
public class inc{
	public static void main (String [] args) {
		int a = 5;
		//post increment
		System.out.println(a++ +" the value of  a post increment");
		System.out.println(a);
		System.out.println(++a +" value of a  pre increment");
		System.out.println(a);
		int e = 89;
		System.out.println(e--  +" the value of e post decrement");
		System.out.println(e);
		System.out.println(--e +" the value of e pre dercrement");
		System.out.println(e);
		
		
	}
}